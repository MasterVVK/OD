### Пошаговый алгоритм для определения, является ли строка палиндромом

# 1. **Преобразование строки**: Привести строку к нижнему регистру и удалить из нее все пробелы и знаки препинания.
# 2. **Определение длины строки**: Получить длину строки.
# 3. **Сравнение символов**: Сравнить первый символ строки с последним, второй - с предпоследним и т.д. до середины строки.
# 4. **Проверка на палиндром**: Если все соответствующие символы совпадают, то строка является палиндромом, иначе - нет.

import string


def is_palindrome(s: str) -> bool:
    # Преобразование строки: приведение к нижнему регистру и удаление знаков препинания и пробелов
    s = s.lower()
    s = s.translate(str.maketrans('', '', string.punctuation)).replace(" ", "")

    # Определение длины строки
    length = len(s)

    # Сравнение символов
    for i in range(length // 2):
        if s[i] != s[length - i - 1]:
            return False

    return True
